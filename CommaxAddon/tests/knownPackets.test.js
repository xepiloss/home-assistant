const test = require('node:test');
const assert = require('node:assert/strict');

const { isKnownIgnoredMeteringFrame, isKnownIgnoredPrimaryFrame } = require('../src/knownPackets');

test('isKnownIgnoredPrimaryFrame recognizes documented Commax query frames', () => {
    assert.equal(isKnownIgnoredPrimaryFrame([0x30, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x34]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x02, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x03]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x79, 0x01, 0x02, 0x00, 0x00, 0x00, 0x00, 0x7C]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x20, 0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x22]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x0F, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x0F]), true);
});

test('isKnownIgnoredPrimaryFrame keeps malformed or unsupported frames visible', () => {
    assert.equal(isKnownIgnoredPrimaryFrame([0x30, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0xFF]), false);
    assert.equal(isKnownIgnoredPrimaryFrame([0x7F, 0x26, 0x04, 0x25, 0x02, 0x18, 0x35, 0x1D]), false);
    assert.equal(isKnownIgnoredPrimaryFrame([0x57, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x58]), false);
});

test('isKnownIgnoredMeteringFrame recognizes short metering auxiliary frames', () => {
    assert.equal(isKnownIgnoredMeteringFrame([0xF7, 0x30, 0x0F, 0x0F, 0x00, 0xC7, 0x0C]), true);
    assert.equal(isKnownIgnoredMeteringFrame([0xF7, 0x30, 0x0F, 0x8F, 0x02, 0x00, 0x1F, 0x5A, 0x40]), true);
    assert.equal(isKnownIgnoredMeteringFrame([0xF7, 0x30, 0x0F, 0x81, 0x00, 0x00, 0x00]), false);
});
