const test = require('node:test');
const assert = require('node:assert/strict');

const { isKnownIgnoredMeteringFrame, isKnownIgnoredPrimaryFrame, isKnownStablePrimaryFrame } = require('../src/knownPackets');

test('isKnownIgnoredPrimaryFrame recognizes documented Commax query frames', () => {
    assert.equal(isKnownIgnoredPrimaryFrame([0x02, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x03]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x0F, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x0F]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x10]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x20, 0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x22]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x30, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x34]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x76, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x77]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x77, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x78]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x79, 0x01, 0x02, 0x00, 0x00, 0x00, 0x00, 0x7C]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x8F, 0x0A, 0x03, 0x05, 0x40, 0x08, 0x46, 0x2F]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0xA4, 0x01, 0x01, 0x00, 0x00, 0xD7, 0x00, 0x7D]), true);
});

test('isKnownIgnoredPrimaryFrame recognizes documented auxiliary/status frames', () => {
    assert.equal(isKnownIgnoredPrimaryFrame([0x11, 0x01, 0x80, 0x00, 0x00, 0x00, 0x00, 0x92]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x23, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x23]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x90, 0x50, 0x50, 0x00, 0x00, 0x00, 0x00, 0x30]), true);
});

test('isKnownIgnoredPrimaryFrame keeps malformed or unsupported frames visible', () => {
    assert.equal(isKnownIgnoredPrimaryFrame([0x30, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0xFF]), false);
    assert.equal(isKnownIgnoredPrimaryFrame([0xA4, 0x01, 0x01, 0x00, 0x00, 0xD7, 0x00, 0xFF]), false);
    assert.equal(isKnownIgnoredPrimaryFrame([0x7F, 0x26, 0x04, 0x25, 0x02, 0x18, 0x35, 0x1D]), false);
    assert.equal(isKnownIgnoredPrimaryFrame([0x57, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x58]), false);
});

test('isKnownStablePrimaryFrame recognizes only unchanged high-frequency frames', () => {
    assert.equal(isKnownStablePrimaryFrame([0xF7, 0x20, 0x01, 0x00, 0x00, 0x00, 0x00, 0x18]), true);
    assert.equal(isKnownStablePrimaryFrame([0x47, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x48]), true);
    assert.equal(isKnownStablePrimaryFrame([0x48, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x49]), true);
    assert.equal(isKnownStablePrimaryFrame([0x48, 0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x4A]), true);
    assert.equal(isKnownStablePrimaryFrame([0x48, 0x01, 0x02, 0x00, 0x00, 0x00, 0x00, 0x4B]), true);
    assert.equal(isKnownStablePrimaryFrame([0xAA, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xAA]), true);

    assert.equal(isKnownStablePrimaryFrame([0x8F, 0x0A, 0x03, 0x05, 0x40, 0x08, 0x46, 0x2F]), false);
    assert.equal(isKnownStablePrimaryFrame([0x8F, 0x0A, 0x03, 0x05, 0x40, 0x04, 0x46, 0xFF]), false);
    assert.equal(isKnownStablePrimaryFrame([0xAA, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0xAB]), false);
});

test('isKnownIgnoredPrimaryFrame ignores checksum-valid unknown 0x8F heartbeat candidates', () => {
    assert.equal(isKnownIgnoredPrimaryFrame([0x8F, 0x0A, 0x03, 0x05, 0x40, 0x04, 0x46, 0x2B]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x8F, 0x0A, 0x03, 0x05, 0x40, 0x08, 0x46, 0x2F]), true);
    assert.equal(isKnownIgnoredPrimaryFrame([0x8F, 0x0A, 0x03, 0x05, 0x40, 0x08, 0x46, 0xFF]), false);
});

test('isKnownIgnoredMeteringFrame recognizes short metering auxiliary frames', () => {
    assert.equal(isKnownIgnoredMeteringFrame([0xF7, 0x30, 0x0F, 0x0F, 0x00, 0xC7, 0x0C]), true);
    assert.equal(isKnownIgnoredMeteringFrame([0xF7, 0x30, 0x0F, 0x8F, 0x02, 0x00, 0x1F, 0x5A, 0x40]), true);
    assert.equal(isKnownIgnoredMeteringFrame([0xF7, 0x30, 0x0F, 0x81, 0x00, 0x00, 0x00]), false);
});
